//Converting strings to upper or lowercase
//When accepting input used in a var changed by a user, you can force either upper or lower case:
var userInput1 = prompt ("enter 'yes' or 'no'.").toUpperCase(); //converts input to upper case
console.log(userInput1);
//using this function method is the same for lowercase
var userInput2 = prompt ("Do you want to take the 'blue' or 'red' pill?").toLowerCase();
console.log(userInput2);